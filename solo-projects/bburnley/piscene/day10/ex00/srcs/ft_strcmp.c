int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i])
	{
		if (!s2[i])
			return (1);
		else if (s2[i] > s1[i])
			return (-1);
		else if (s1[i] > s2[i])
			return (1);
		else
			;
		i++;
	}
	if (!s2)
		return (-1);
	else
		return (0);
}
