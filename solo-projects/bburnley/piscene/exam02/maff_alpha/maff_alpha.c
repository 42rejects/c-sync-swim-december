#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		main(void)
{
	int		i;

	i = 0;
	while (i < 26)
	{
		if (i % 2 == 0)
			ft_putchar(i + 97);
		else
			ft_putchar(i + 65);
		i++;
	}
	ft_putchar('\n');
}
