#include <stdio.h>

char	*ft_rot42(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
		str[i] += 16;
		i++;
	return (str);
}

int		main(void)
{
	printf("%s", ft_rot42("This is the code"));
	return (0);
}
